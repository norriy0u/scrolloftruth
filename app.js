function initApp() {
  const key = document.getElementById('apiKeyInput').value.trim();
  if (!key) { alert('A key is required to unfurl the scroll.'); return; }
  apiKey = key;
  localStorage.setItem('claude_api_key_scroll', key);
  startScroll();
}

function skipToOffline() {
  apiKey = null;
  startScroll();
  scrollTrigger.innerHTML = '<div class="loader-text">The oracle slumbers. No new truths will be generated.</div>';
}

function startScroll() {
  screenApi.classList.remove('active');
  screenScroll.classList.add('active');
  
  // Render Initial
  INITIAL_TRUTHS.forEach(t => renderTruth(t));
  
  // Render local custom truths if any
  const custom = JSON.parse(localStorage.getItem('scroll_custom_truths')) || [];
  custom.forEach(t => renderTruth(t));
  
  // Setup Observer
  if (apiKey) {
    const observer = new IntersectionObserver((entries) => {
      if (entries[0].isIntersecting && !isFetching) {
        fetchNewTruths();
      }
    }, { rootMargin: '200px' });
    observer.observe(scrollTrigger);
  }

  initAudio();
}

