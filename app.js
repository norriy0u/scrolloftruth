/* ── WEB AUDIO AMBIENCE ── */
let audioEnabled = false;
let droneOsc, gainNode;

function toggleAudio() {
  if (!audioCtx) audioCtx = new (window.AudioContext || window.webkitAudioContext)();
  
  if (audioEnabled) {
    audioCtx.suspend();
    document.getElementById('audioToggle').style.opacity = '0.5';
  } else {
    audioCtx.resume();
    if (!droneOsc) startAmbience();
    document.getElementById('audioToggle').style.opacity = '1';
  }
  audioEnabled = !audioEnabled;
}

function startAmbience() {
  // 40Hz Drone
  droneOsc = audioCtx.createOscillator();
  gainNode = audioCtx.createGain();
  droneOsc.type = 'sine';
  droneOsc.frequency.value = 40; // Very low rumble
  gainNode.gain.value = 0.05;
  droneOsc.connect(gainNode).connect(audioCtx.destination);
  droneOsc.start();
  
  // Bell Toll Loop
  scheduleBell();
}

function scheduleBell() {
  const nextToll = Math.random() * 45000 + 45000; // 45-90s
  setTimeout(() => {
    playBell();
    scheduleBell();
  }, nextToll);
}

function playBell() {
  if (!audioCtx || audioCtx.state !== 'running') return;
  const t = audioCtx.currentTime;
  
  const osc1 = audioCtx.createOscillator();
  const osc2 = audioCtx.createOscillator();
  const osc3 = audioCtx.createOscillator();
  const gain = audioCtx.createGain();
  
  // Metallic detuned frequencies
  osc1.frequency.value = 150;
  osc2.frequency.value = 150 * 2.1;
  osc3.frequency.value = 150 * 3.14;
  
  osc1.connect(gain);
  osc2.connect(gain);
  osc3.connect(gain);
  gain.connect(audioCtx.destination);
  
  // Slow decay
  gain.gain.setValueAtTime(0, t);
  gain.gain.linearRampToValueAtTime(0.08, t + 0.1);
  gain.gain.exponentialRampToValueAtTime(0.001, t + 5);
  
  osc1.start(t); osc2.start(t); osc3.start(t);
  osc1.stop(t + 5); osc2.stop(t + 5); osc3.stop(t + 5);
}
